package com.viddu.content.bo;

public class ExerciseRoutine {
    private final String name;
    private final Integer id;

    public ExerciseRoutine(String name, Integer id) {
        super();
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public Integer getId() {
        return id;
    }

}
